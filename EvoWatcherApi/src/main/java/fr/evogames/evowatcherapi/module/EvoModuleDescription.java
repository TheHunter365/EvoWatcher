package fr.evogames.evowatcherapi.module;

public class EvoModuleDescription {

    private String moduleName, author, mainClass, version;

    public String getModuleName() {
        return moduleName;
    }

    public String getAuthor() {
        return author;
    }

    public String getMainClass() {
        return mainClass;
    }

    public String getVersion() {
        return version;
    }
}
